// Create an array, display its elements, and update a value

// array 
let courses = ["English", "Urdu", "Maths"];

// print array
console.log("Courses:", courses);

// access all elements
console.log("First course:", courses[0]);
console.log("Second course:", courses[1]);
console.log("Third course:", courses[2]);

// update value
courses[1] = "computer";

// print updated array
console.log("Updated courses:", courses);