// using all loops to print numbers from 1 to 3

// for loop
console.log("For Loop:");
for (let i = 1; i <= 3; i++) {
    console.log(i);
}

// while loop
console.log("While Loop:");
let i = 1;
while (i <= 3) {
    console.log(i);
    i++;
}

// do-while loop
console.log("Do-While Loop:");
let num = 1;
do {
    console.log(num);
    num++;
} while (num <= 3);