//  using all conditions to check student grade

let studentName = "Sara";
let marks = 75;

// if condition
if (marks >= 80) {
    console.log(studentName + " Grade: A");
}
 // else if condition
else if (marks >= 60) {
    console.log(studentName + " Grade: B");
} 
else if (marks >= 40) {
    console.log(studentName + " Grade: C");
}
 // else condition
else {
    console.log(studentName + " has failed");
}